// ============================================
// 과목명 정규화 — 단일 정본
//
// 공백/특수문자/로마숫자 차이를 흡수하여 과목명 비교용 키를 생성.
// 프로젝트 내 모든 과목명 비교 로직이 이 함수를 사용해야 한다.
// ============================================

/**
 * 과목명을 정규화 키로 변환
 *
 * 처리 순서:
 * 1. 양쪽 공백 제거 (trim)
 * 2. 내부 공백 전체 제거
 * 3. 유니코드 로마숫자 → 아라비아 숫자 (Ⅰ~Ⅳ)
 * 4. ASCII 로마숫자 → 아라비아 숫자 (끝자리 I~IV)
 * 5. 가운뎃점 계열 문자 제거 (·, ‧, ・)
 * 6. 괄호 및 괄호 내용 제거
 * 7. 대괄호 제거
 * 8. 소문자 변환
 */
export function normalizeSubjectName(name: string): string {
  return name
    .trim()
    .replace(/\s+/g, "")
    // 유니코드 로마숫자
    .replace(/Ⅰ/g, "1")
    .replace(/Ⅱ/g, "2")
    .replace(/Ⅲ/g, "3")
    .replace(/Ⅳ/g, "4")
    // ASCII 로마숫자 (끝자리, longest-first)
    .replace(/(?<=\D)IV$/i, "4")
    .replace(/(?<=\D)III$/i, "3")
    .replace(/(?<=\D)II$/i, "2")
    .replace(/(?<=\D)I$/i, "1")
    // 가운뎃점 제거
    .replace(/[·‧・]/g, "")
    // 괄호 내용 + 괄호 자체 제거
    .replace(/\(.*?\)/g, "")
    .replace(/[()[\]]/g, "")
    .toLowerCase();
}
