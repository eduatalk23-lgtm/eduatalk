/** @verified 2026-03-29 @source 컨설턴트 실무 학생 분류 패턴 */
function buildStudentArchetypes(): string {
  return `
### 학생 아키타입별 맞춤 전략
학생 데이터를 보고 아래 유형 중 가장 가까운 것을 판별하고, 해당 전략을 기반으로 조언하세요.

**A. 성적 우등생** (내신 1등급대, 세특 평범)
- 핵심 문제: 성적은 좋지만 "왜?"가 없는 기록. 평가자에게 "공부만 하는 학생"으로 보일 위험
- 전략: 교과전형 안정 확보 + 종합전형을 위해 세특 1~2과목만 집중 심화. 전공 관련 탐구 추가
- 주의: 모든 과목 세특을 올리려 하지 말고 핵심 과목에 집중

**B. 활동형 학생** (내신 2~3등급, 비교과 풍부)
- 핵심 문제: 활동은 많지만 스토리라인이 산만할 수 있음
- 전략: 종합전형 주력. 활동을 하나의 진로 서사로 엮기. 가장 강한 활동 2~3개를 축으로 정리
- 주의: 새 활동 추가보다 기존 활동의 깊이와 연결성 강화

**C. 과학 특화 학생** (과학고/영재, R&E 경험)
- 핵심 문제: 연구는 깊지만 인문/공동체 역량이 약할 수 있음
- 전략: 과기원(KAIST 등) 별도 전형 + SKY 자연계열 종합 병행. R&E를 세특 스토리라인의 핵심으로
- 주의: 의대 지원 시 생명윤리/봉사 등 인성 영역 보완 필수

**D. 만능형 학생** (내신 1~2등급 + 비교과 풍부)
- 핵심 문제: 강점이 많아서 오히려 초점이 흐려질 수 있음
- 전략: SKY 종합 도전 + 교과 안전망. 가장 차별화되는 활동 1개를 메인 스토리로 선택
- 주의: "다 잘한다"가 아니라 "이것을 가장 잘한다"는 포커싱 필요

**E. 전환기 학생** (진로 변경, 스토리라인 단절)
- 핵심 문제: 1학년과 2~3학년의 활동 방향이 다름
- 전략: 변경 자체를 성장 서사로 재해석. "탐색→발견→확신"의 프레임으로 전환 과정을 설명
- 주의: 이전 활동을 부정하지 말고 새 진로의 기반으로 연결

**F. 내신 불리 학생** (내신 3~4등급, 자사고/외고 포함)
- 핵심 문제: 내신만으로는 경쟁력 부족
- 전략: 종합전형에서 세특+스토리라인으로 승부 + 논술/정시 병행. 자사고/외고는 학교 특성으로 내신 보정 어필
- 주의: 현실적 대학 목표 설정 필수. 상향 지원은 1~2장만`;
}

// ── J. 교육과정별 등급 체계 ──

/** @verified 2026-03-29 @source 교육부 고시 제2022-33호, 2015 개정교육과정 총론 */
function buildCurriculumGradingContext(curriculum?: string | null): string {
  const is2022 = curriculum?.includes("2022");

  if (is2022) {
    return `
### 교육과정 등급 체계: 2022 개정 (이 학생에 해당)
- **일반선택과목**: 9등급 상대평가 (1~9등급, 기존과 동일)
- **진로선택과목**: 성취평가제 A/B/C (절대평가, 등급 없음)
- **융합선택과목**: 성취평가제 A/B/C (절대평가)
- ⚠️ **핵심 주의**: 진로선택 A등급은 학교마다 A비율이 다름 (10%~40%). 단순히 "A등급"만으로 우수성을 판단하면 안 됨
- **내신 산출 시**: 일반선택 9등급만 정량적으로 비교 가능. 진로선택은 성취도+이수자 수+A비율을 종합 고려
- **대학 반영 방식**: 대학마다 진로선택 A/B/C 반영 방법이 다름. 일부 대학은 A=1등급 환산, 일부는 별도 가산점
- **전략적 함의**: 진로선택 과목은 "A를 받는 것"보다 "어떤 과목을 선택했는가"가 더 중요. 전공 관련 진로선택 이수 자체가 평가 요소`;
  }

  if (curriculum?.includes("2015")) {
    return `
### 교육과정 등급 체계: 2015 개정 (이 학생에 해당)
- **일반선택과목**: 9등급 상대평가
- **진로선택과목**: 3단계 성취평가 (A/B/C) + 석차등급 병기
- **내신 비교**: 일반선택과 진로선택 모두 9등급 상대평가 기반이므로 직접 비교 가능
- **전략적 함의**: 진로선택도 등급이 산출되므로, 전공 관련 진로선택 과목의 등급 관리가 중요`;
  }

  // 교육과정 미지정 시 양쪽 모두 안내
  return `
### 교육과정별 등급 체계 차이 (학생 교육과정 미확인)
- **2015 개정**: 일반선택/진로선택 모두 9등급 상대평가. 직접 비교 가능
- **2022 개정**: 일반선택은 9등급 상대평가, 진로선택/융합선택은 A/B/C 성취평가제(절대평가)
- ⚠️ 2022 학생의 경우 진로선택 A등급은 학교별 A비율이 달라 단순 비교 불가
- 학생의 교육과정을 먼저 확인한 후 등급 해석에 반영하세요`;
}
