/**
 * 세션 대화 내역으로 평가 요청 프롬프트 생성
 */
export function buildEvaluationUserPrompt(
  studentProfile: string,
  conversationTranscript: string,
): string {
  return `## 학생 프로필
${studentProfile}

## AI 컨설턴트의 대화 내역
아래는 AI 컨설턴트가 위 학생에 대해 수행한 분석의 전체 대화 기록입니다.
도구 호출과 결과, 최종 응답이 포함되어 있습니다.

${conversationTranscript}

---
위 대화를 평가 기준에 따라 엄격하게 평가하고, JSON 형식으로 결과를 제공해주세요.`;
}
