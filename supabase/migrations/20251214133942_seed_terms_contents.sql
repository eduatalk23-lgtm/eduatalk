## 제6장 계약 당사자의 의무

### 제26조 (회원의 의무)

1. 회원은 서비스 이용을 위해 가입 시 현재의 사실과 일치하는 완전한 정보(이하 "가입정보"라 한다)를 제공해야 합니다. 또한 가입정보가 변경된 경우 즉시 갱신해야 합니다.
2. 회원은 자신이 설정한 ID 및 PASSWORD를 안전하게 관리하여야 하며 ID 및 PASSWORD 관리를 위하여 다음 각 호와 같은 행위를 수행할 의무가 있습니다.
   - 회원의 승인 없이 ID, PASSWORD가 사용되는 문제가 발생한 경우 즉시 회사에 신고
   - 회사가 권고하거나 회원이 필요하다고 판단한 경우 주기적인 비밀번호의 변경
   - 매 접속 종료 시 확실한 로그아웃
3. 이 약관에 동의한 회원은 서비스 이용 중 다음과 같은 행위를 하지 않는 것에 동의한 것으로 간주합니다.
   - 1인 이상의 타인에게 위해를 가하는 행위
     - 타인의 ID, PASSWORD, 주민등록번호 도용 및 타인으로 가장하는 행위
     - 타인이 서비스 이용으로 얻은 정보를 정상적인 서비스 이용이 아닌 방법으로 획득하는 경우
     - 서비스의 결과물을 이용하여 타인에게 손해를 가하거나 그와 유사한 목적으로 서비스를 이용하는 경우
     - 타인을 비방할 목적으로 사실 또는 허위의 사실을 적시하여 명예를 훼손하는 행위
     - 본인 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 가할 목적으로 서비스 관련 정보 또는 허위사실을 유통시키는 행위
     - 회사의 사전 승낙 없이 서비스를 이용한 영리행위
   - 서비스의 기본 기능을 우회하거나 기술적 취약점을 이용하여 공격하는 행위
   - 권리(지적재산권을 포함한 모든 권리)가 없는 컨텐츠를 게시, 게재 또는 전자우편으로 보내는 행위
   - 컴퓨터 소프트웨어, 하드웨어, 전기통신 장비를 파괴, 방해 또는 기능을 제한하기 위한 소프트웨어 바이러스를 게시, 게재 또는 전자우편으로 보내는 행위
   - 다른 컴퓨터 코드, 파일, 프로그램을 포함하고 있는 자료를 게시, 게재, 전자우편으로 보내는 행위 등 다른 사용자의 개인정보를 수집 또는 저장하는 행위
   - 기타 불법적이거나 부당한 행위
4. 회원은 이 약관 및 관계법령에서 규정한 사항을 준수해야 합니다.

### 제27조 (회사의 의무)

1. 회사는 특별한 사정이 없는 한 회원이 신청한 서비스 제공 개시일에 서비스를 이용할 수 있도록 해야합니다.
2. 회사는 이 약관에서 정한 바에 따라 계속적, 안정적으로 서비스를 제공할 의무가 있습니다.
3. 회사가 제공하는 서비스로 인하여 회원에게 손해가 발생한 경우 회사의 고의 또는 중과실에 의한 경우에 한하여 회사가 책임을 부담하며, 그 책임의 범위는 통상손해로 제한합니다. 이 경우 회사의 고의 또는 중과실이 있었다는 사실의 입증 책임은 회원에게 있습니다.
4. 회사는 제3항에 대하여 책임을 부담하지 않으려는 경우 회사의 고의 또는 중과실이 없었다는 사실을 입증하여야 합니다.
5. 회사는 회원으로부터 제기되는 의견이나 불만이 정당하다고 인정할 경우에는 신속히 처리해야 합니다. 다만, 신속한 처리가 곤란한 경우에는 회원에게 그 사유와 처리일정을 통보해야 합니다.
6. 회사는 개인정보보호법 등 관련 법령이 정하는 바에 따라서 회원 등록정보를 포함한 회원의 개인정보를 보호하기 위하여 노력해야 합니다.

