## 제5장 계약의 해지 기타

### 제23조 (계약의 합의해지)

1. 서비스 이용계약은 언제나 해지될 수 있으며 회원의 해지신청과 이에 대한 회사의 해지처리로 계약은 해지됩니다.
2. 회원이 서비스이용 계약을 해지하고자 하는 때에는 회원 본인이 직접 서비스상으로 또는 서면으로 회사에 해지신청을 접수하여야 합니다.
3. 해지신청이 접수되면 회사는 익일부터 서비스이용을 제한하고 더 이상 서비스 이용요금을 부과하지 않습니다.
4. 해지신청자가 해지신청일까지의 모든 이용요금을 완납한 경우에 회사는 해지처리를 해야 합니다.

### 제24조 (계약의 일방해지)

1. 회사의 이용요금 등의 청구에 대하여 이용자가 최초 청구일 이후 3개월 이내에 당해 청구분을 완납하지 않을 때에는 회사는 서비스 이용계약을 일방해지 할 수 있습니다.
2. 이용자의 서비스 이용내용 및 행위가 제18조 각호의 1에 해당하고 그 정도가 중한 경우에 회사는 이용계약을 일방해지할 수 있습니다.

### 제25조 (양도등의 금지)

회사에 별도로 허락받는 경우를 제외하고는 회원은 서비스 받을 권리를 제3자에게 양도하거나 증여 등을 할 수 없으며, 또한 질권의 목적으로도 사용할 수 없습니다.

