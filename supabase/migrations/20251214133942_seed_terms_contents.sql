## 제4장 서비스 요금

### 제20조 (서비스 요금)

회원이 납입하여야 하는 요금의 종류는 다음 각 호와 같습니다.

1. 기간제 요금: 특정 기간 단위로 제공되는 서비스를 이용함에 따라 납입하여야 하는 요금
2. 서비스 요금: 1회성 서비스 이용에 따라 납입하여야 하는 요금
3. 구독형 요금: 월별로 홈페이지 내의 모든 유료 콘텐츠를 사용하기 위해 납입하여야 하는 요금

※ 현재는 무료로 제공되며, 향후 유료 서비스가 추가될 수 있습니다.

### 제21조 (요금의 납입)

1. 요금의 납입은 신용카드 결제, 무통장 입금 등 회사가 제시한 방법 중 회원이 선택한 방법으로 할 수 있습니다. 단, 회원이 기존 요금의 납입 방법을 변경하고자 하는 경우 회사와 협의 또는 직접 홈페이지에서 변경하여야 합니다.
2. 회사는 회원에게 회사가 지정하는 기일에 요금 등을 납입하도록 청구할 수 있습니다.
3. 회사는 회원에게 책임이 없는 사유로 인하여 서비스를 제공받지 못한 경우로서 회원이 그 뜻을 회사에 통지한 경우 요금을 반환할 수 있습니다. 단, 반환되는 요금은 당 월 또는 당 회에 한하며, 홈페이지에 공지한 해당 상품의 환불정책에 따라 처리됩니다.

### 제22조 (요금의 연체, 미납)

기한내에 서비스 요금 등을 납입하지 않는 회원에 대하여 회사는 서비스 이용을 제한 및 중지할 수 있습니다.

