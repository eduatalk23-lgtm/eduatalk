   - 서비스 성격에 부합하지 않는 정보를 입력 또는 등록하는 경우
   - 기타 관계 법령 및 회사에서 정한 규정 등에 위배되는 경우
2. 회사는 회원의 정보처리시스템(스마트폰, PC 등)의 보안 취약점에 의하여 서비스 전체에 영향이 있다고 판단하는 경우 특정 회원의 서비스 사용을 중지할 수 있습니다. 이 경우 회원은 필요한 보안 요건을 만족하여야만 서비스의 계속 이용이 가능합니다.

### 제19조 (서비스 제공의 중지)

1. 국가의 비상사태, 정전 등 회사의 관리범위를 벗어난 재해 또는 재난 등의 상황에 따라 서비스의 변경, 중지 및 정보의 손실이 발생한 경우 회사는 아무런 책임을 지지 않습니다.
2. 회사는 제1항의 상황이 발생하더라도 서비스의 정상적인 제공이 되거나 정상적인 서비스 제공까지의 시간을 단축하기 위하여 노력을 다해야 합니다.
3. 서비스는 회사의 업무상 또는 기술상의 이유로 일시 중지될 수 있으며, 정기점검 등 운영상의 목적으로 회사가 사전 정하여 고지한 기간에는 서비스가 일시 중지될 수 있습니다.
4. 회사는 회사의 사정으로 서비스를 영구적으로 중단하여야 할 경우 회원에게 반드시 고지해야 합니다.

