## 제7장 기타

### 제28조 (양도금지)

회원은 서비스의 이용권한, 기타 이용 계약상의 지위를 타인에게 양도, 증여할 수 없으며, 이를 담보로 제공할 수 없습니다.

### 제29조 (손해배상)

회원은 이 약관의 내용에 위반한 행위에 의하여 회사에 손해가 발생한 경우 그 손해를 배상하여야 합니다.

### 제30조 (면책조항)

1. 회사는 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.
2. 회사는 회원의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.
3. 회사는 회원이 서비스를 이용하여 기대하는 이익이나 서비스를 통하여 얻은 자료로 인한 손해에 관하여 책임을 지지 않습니다.
4. 회사는 회원이 입력한 성적 데이터나 학습 정보의 정확성에 대해 책임을 지지 않으며, 이로 인한 추천 결과나 분석 결과의 오류에 대해서도 책임을 지지 않습니다.

### 제31조 (관할법원)

1. 서비스 이용과 관련하여 회사와 회원 사이에 분쟁이 발생한 경우, 회사와 회원은 발생한 분쟁을 원만하게 해결하기 위하여 필요한 모든 노력을 기울여야 합니다.
2. 제1항의 규정에도 불구하고 서비스 이용으로 발생한 분쟁에 대하여 소송이 제기될 경우 서울중앙지방법원을 관할법원으로 합니다.

